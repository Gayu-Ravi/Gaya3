public class TypeConversion3
{
	public static void main(String args[])
	{
		System.out.println("Float to Double Conversion");
		float a=458.86f;
		double b=a;
		System.out.println("Given value :" + a);
		System.out.println("Result      :" + b);
		System.out.println("Double to Float Conversion");
		double g=55.456789999;
		float d=(float)g;
		System.out.println("Given value :" + g);
		System.out.println("Result      :" + d);
	}
}